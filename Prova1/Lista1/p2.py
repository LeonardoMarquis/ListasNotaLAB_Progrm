def main():

    num1 = int(input("Primeiro Numero Inteiro:\n"))
    num2 = int(input("\nSegundo Numero Inteiro:\n"))

    print(f"O primeiro número: {num1}")
    print(f"O segundo número: {num2}")

    print(f"A soma: {num1 + num2}")
    print(f"O Produto: {num1 * num2}")
    print(f"A subtracao do primeiro e segundo: {num1 - num2}")
    print(f"O primeiro dividido pelo segundo: {num1/num2}")

if __name__ == "__main__":
    main()